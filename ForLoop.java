package Loops;

public class ForLoop {


    public static void main(String[] args) {
        System.out.println("10 Even Number");
        for(int i=1; i<=10; i++){
            System.out.println(i*2);
        }

        System.out.println("10 Odd Number");
        for (int i=1;i<=10;i++){
            System.out.println(i*2-1);
        }
    }
}